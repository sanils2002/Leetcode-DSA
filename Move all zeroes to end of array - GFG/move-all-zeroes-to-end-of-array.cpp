//User function template for C++
class Solution{
public:
	void pushZerosToEnd(int nums[], int n) {
	    // code here
	    for(int lastNonZeroFoundAt = 0, idx = 0 ; idx < n ; idx++) {
            if(0 != nums[idx])
                swap(nums[lastNonZeroFoundAt++], nums[idx]);
        }
	}
};

